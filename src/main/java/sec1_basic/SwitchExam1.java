package sec1_basic;

public class SwitchExam1 {
    public static void main(String[] args){
        int num = 2;
        switch(num){
            case 1 :
                System.out.println(1);
            case 2 :
                System.out.println(2); // @@ 2
            case 3:
                System.out.println(3); // @@ 3
        }
    }
}
